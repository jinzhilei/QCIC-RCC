function cmap = Color_RdYlBu(n)

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%% Color: this file is used to provide drawing colors
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

    if nargin < 1, n = 256; end
    pos = (0:0.1:1)';
    rgb = [165, 0, 38;
           215, 48, 39;
           244, 109, 67;
           253, 174, 97;
           254, 224, 139;
           255, 255, 191;
           224, 243, 248;  
           171, 217, 233;   
           116, 173, 209;  
            69, 117, 180;   
            49,  54, 149] / 255; 
    xi = linspace(0, 1, n)';
    rgb = flipud(rgb);
    cmap = interp1(pos, rgb, xi, 'pchip');
end